# program konversi celcius ke satuan yang lain

celcius = float(input('masukan suhu celcius : '))
print("suhu celcius :",celcius)

# reamur
reamur = 4/5 * celcius
print("reamur :",reamur)

# fahrenheit
fahrenheit = 9/5 * celcius + 32
print("fahrenheit :",fahrenheit)

# kelvin
kelvin = celcius + 273
print("kelvin :",kelvin)